    public ArrayList<Registration> patientList()
    {
        ArrayList<Registration> patientsList = new ArrayList<>();
        Connection conn = MySqlDatabase.ConnectDB();
        String query = "SELECT * FROM patient_table";
        try {
        PreparedStatement ps = conn.prepareStatement(query);
        ResultSet rs = ps.executeQuery(query);
        
        Registration registration;
        while(rs.next())
        {
            registration = new Registration(rs.getString ("patientID"), rs.getString ("patientName"), rs.getString ("icNumber"), rs.getString ("gender"), rs.getString ("date"), rs.getString ("address"), rs.getString ("mobilePhone"), rs.getString ("emailAddress"));
            patientsList.add(registration);
        }
        }catch(Exception e)
         {
           JOptionPane.showMessageDialog(null, e);
         }
        return patientsList;
    }
    
    public void show_patient()
    {
        ArrayList<Registration> list = patientList();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Object[] row = new Object[11];
        for(int i=0; i<list.size(); i++)
        {
            row[0] = list.get(i).getPatientID();
            row[1] = list.get(i).getPatientName();
            row[2] = list.get(i).getIcNumber();
            row[3] = list.get(i).getGender();
            row[4] = list.get(i).getDate();
            row[5] = list.get(i).getAddress();
            row[6] = list.get(i).getMobilePhone();
            row[7] = list.get(i).getEmailAddress();
            model.addRow(row);   
        }
    }

