    private void jbtnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnInsertActionPerformed
        Connection conn = MySqlDatabase.ConnectDB();
        String query = "INSERT INTO patient_table(patientID, patientName, icNumber, gender, date, address, mobilePhone, emailAddress)" + "values(?,?,?,?,?,?,?,?)";
        try{
            

            PreparedStatement ps = conn.prepareStatement(query);
            ps.setString(1, jtxtPatientID.getText());
            ps.setString(2, jtxtPatientName.getText());
            ps.setString(3, jtxtICNo.getText());
            
            if(jradBtnMale.isSelected())
            {
                Gender = "Male";
            }
            if(jradBtnFemale.isSelected())
            {
                Gender = "Female";
            }
            ps.setString(4, Gender);
            
            ps.setString(5, jtxtDate.getText());
            ps.setString(6, jtxtAddress.getText());
            ps.setString(7, jtxtMobilePhone.getText());
            ps.setString(8, jtxtEmailAddress.getText());

            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data successfully insert");
            
            RegisterForm registerForm = new RegisterForm();
            registerForm.setVisible(true);
            dispose();
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jbtnInsertActionPerformed

