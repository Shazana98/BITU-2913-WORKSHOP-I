    private void jbtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnUpdateActionPerformed
        if(jtxtPatientID.getText() != null)
        {
            String UpdateQuery = null;
            PreparedStatement ps = null;
            Connection conn = MySqlDatabase.ConnectDB();
        
            try{
                UpdateQuery = "UPDATE patient_table SET patientName = ?, icNumber = ?, gender = ?, date = ?, address = ?, mobilePhone = ?, emailAddress = ? WHERE patientID = ?";
                ps = conn.prepareStatement(UpdateQuery);
                ps.setString(1, jtxtPatientName.getText());
                ps.setString(2, jtxtICNo.getText());
                
                if(jradBtnMale.isSelected())
                {
                    Gender = "Male";
                }
                if(jradBtnFemale.isSelected())
                {
                    Gender = "Female";
                }
                ps.setString(3, Gender);
                
                ps.setString(4, jtxtDate.getText());
                ps.setString(5, jtxtAddress.getText());
                ps.setString(6, jtxtMobilePhone.getText());
                ps.setString(7, jtxtEmailAddress.getText());
            
                ps.setString(8, jtxtPatientID.getText());
                ps.executeUpdate();
            
                JOptionPane.showMessageDialog(null, "Data Successfully Update!");
            
                RegisterForm registerForm = new RegisterForm();
                registerForm.setVisible(true);
                dispose();
            }catch(Exception e)
             {
                JOptionPane.showMessageDialog(null, e);
             }

        }
    }//GEN-LAST:event_jbtnUpdateActionPerformed

