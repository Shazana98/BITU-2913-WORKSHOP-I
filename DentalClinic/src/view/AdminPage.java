    private void jBtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAddActionPerformed
        Connection conn = MySqlDatabase.ConnectDB();
        String query = "INSERT INTO staff_table(staffID, startingDate, icNumber, name, gender, position, username, password, address, phoneNumber, emailAddress) VALUES (?,?,?,?,?,?,?,?,?,?,?)";
        String Password = new String(jPassword.getPassword());
        String Confirm = new String(jRetypePassword.getPassword());

        try{
            PreparedStatement ps = conn.prepareStatement(query);
            ps.setString(1, jTxtStaffID.getText());
            ps.setString(2, jTxtDate.getText());
            ps.setString(3, jTxtICNum.getText());
            ps.setString(4, jTxtName.getText());

            if(jRadBtnMale.isSelected())
            {
                Gender = "Male";
            }
            if(jRadBtnFemale.isSelected())
            {
                Gender = "Female";
            }
            ps.setString(5, Gender);

            ps.setString(6, (String) jComboBox1.getSelectedItem());
            ps.setString(7, jTxtUsername.getText());

            ps.setString(8, Password);
            ps.setString(8, Confirm);

            ps.setString(9, jTxtAddress.getText());
            ps.setString(10, jTxtPhone.getText());
            ps.setString(11, jTxtEmail.getText());

            if(Password.equals(Confirm))
            {
                ps.execute();
                ps.close();

                JOptionPane.showMessageDialog(null, "Your username and password already saved!");
                JOptionPane.showMessageDialog(null, "Successfully adding new staff's data!");

                AdminPage adminPage = new AdminPage();
                adminPage.setVisible(true);
                dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Your Password does not match.");
            }
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jBtnAddActionPerformed

