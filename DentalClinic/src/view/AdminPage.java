    private void jBtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnUpdateActionPerformed
        if(jTxtStaffID.getText() != null)
        {
            String UpdateQuery = null;
            PreparedStatement ps = null;
            Connection conn = MySqlDatabase.ConnectDB();
            String Password = new String(jPassword.getPassword());
            String Confirm = new String(jRetypePassword.getPassword());

            try {
                UpdateQuery = "UPDATE staff_table SET startingDate = ?, icNumber = ?, name = ?, gender = ?, position = ?, username = ?, password = ?, address = ?, phoneNumber = ?, emailAddress = ? WHERE staffID = ?";
                ps = conn.prepareStatement(UpdateQuery);
                ps.setString(1, jTxtDate.getText());
                ps.setString(2, jTxtICNum.getText());
                ps.setString(3, jTxtName.getText());

                if(jRadBtnMale.isSelected())
                {
                    Gender = "Male";
                }
                if(jRadBtnFemale.isSelected())
                {
                    Gender = "Female";
                }
                ps.setString(4, Gender);

                ps.setString(5, (String) jComboBox1.getSelectedItem());
                ps.setString(6, jTxtUsername.getText());

                ps.setString(7, Password);
                ps.setString(7, Confirm);

                ps.setString(8, jTxtAddress.getText());
                ps.setString(9, jTxtPhone.getText());
                ps.setString(10, jTxtEmail.getText());

                ps.setString(11, jTxtStaffID.getText());

                ps.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Staff's data updated");
                
                AdminPage adminPage = new AdminPage();
                adminPage.setVisible(true);
                dispose();
            } catch (Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jBtnUpdateActionPerformed

