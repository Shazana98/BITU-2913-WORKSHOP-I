    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int i = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        
        jTxtStaffID.setText(model.getValueAt(i,0).toString());
        jTxtDate.setText(model.getValueAt(i,1).toString());
        jTxtICNum.setText(model.getValueAt(i,2).toString());
        jTxtName.setText(model.getValueAt(i,3).toString());
        String Gender = model.getValueAt(i,4).toString();
        if (Gender.equals("Male"))
        {
            jRadBtnMale.setSelected(true);
            jRadBtnFemale.setSelected(false);
        }
        else
        {
            jRadBtnFemale.setSelected(true);
            jRadBtnMale.setSelected(false);
        }
        String position = model.getValueAt(i,5).toString();
        switch(position)
        {
            case "Admin":
            jComboBox1.setSelectedIndex(1);
            break;
            case "Dentist":
            jComboBox1.setSelectedIndex(2);
            break;
            case "Nurse":
            jComboBox1.setSelectedIndex(3);
            break;
        }
        jTxtUsername.setText(model.getValueAt(i,6).toString());
        jPassword.setText(model.getValueAt(i,7).toString());
        jRetypePassword.setText(model.getValueAt(i,7).toString());
        jTxtAddress.setText(model.getValueAt(i,8).toString());
        jTxtPhone.setText(model.getValueAt(i,9).toString());
        jTxtEmail.setText(model.getValueAt(i,10).toString());
    }//GEN-LAST:event_jTable1MouseClicked

