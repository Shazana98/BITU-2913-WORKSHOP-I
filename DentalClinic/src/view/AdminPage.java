    public ArrayList<NewStaff> newStaffList()
    {
        ArrayList<NewStaff> staffList = new ArrayList<>();
        Connection conn = MySqlDatabase.ConnectDB();
        String query = "SELECT * FROM staff_table";
        try {
        PreparedStatement ps = conn.prepareStatement(query);
        ResultSet rs = ps.executeQuery(query);
        
        NewStaff newStaff;
        while(rs.next())
        {
            newStaff = new NewStaff(rs.getString("staffID"), rs.getString("startingDate"), rs.getString("icNumber"), rs.getString("name"), rs.getString("gender"), rs.getString("position"), rs.getString("username"), rs.getString("password"), rs.getString("address"), rs.getString("phoneNumber"), rs.getString("emailAddress"));
            staffList.add(newStaff);
        }
        }catch(Exception e)
         {
           JOptionPane.showMessageDialog(null, e);
         }
        return staffList;
    }
    
    public void show_NewStaff()
    {
        ArrayList<NewStaff> list = newStaffList();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Object[] row = new Object[11];
        for(int i=0; i<list.size(); i++)
        {
            row[0] = list.get(i).getStaffID();
            row[1] = list.get(i).getStartingDate();
            row[2] = list.get(i).getIcNumber();
            row[3] = list.get(i).getName();
            row[4] = list.get(i).getGender();
            row[5] = list.get(i).getPosition();
            row[6] = list.get(i).getUsername();
            row[7] = list.get(i).getPassword();
            row[8] = list.get(i).getAddress();
            row[9] = list.get(i).getPhoneNumber();
            row[10] = list.get(i).getEmailAddress();
            model.addRow(row);   
        }
    }
