    private void updateCombo() {
        Connection conn = MySqlDatabase.ConnectDB();
        String query = "SELECT * FROM patient_table";
        try{
            PreparedStatement ps = conn.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            
            while(rs.next())
            {
                jComboBox1.addItem(rs.getString("patientID"));
            }
        }catch(Exception e)
        {
           JOptionPane.showMessageDialog(null, e);
        }
        
        String query2 = "SELECT * FROM patient_table";
        try{
            PreparedStatement ps = conn.prepareStatement(query2);
            ResultSet rs = ps.executeQuery();
            
            while(rs.next())
            {
                jComboBox2.addItem(rs.getString("patientName"));
            }
        }catch(Exception e)
        {
           JOptionPane.showMessageDialog(null, e);
        }
        
        String query3 = "SELECT * FROM treatment_table";
        try{
            PreparedStatement ps = conn.prepareStatement(query3);
            ResultSet rs = ps.executeQuery();
            
            while(rs.next())
            {
                jComboBox3.addItem(rs.getString("treatment"));
            }
        }catch(Exception e)
        {
           JOptionPane.showMessageDialog(null, e);
        }
    }
