    private void jbtnPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPrintActionPerformed
        try{
            Document doc = new Document();
            try{
                PdfWriter.getInstance(doc, new FileOutputStream("Receipt.pdf"));
            } catch (FileNotFoundException ex)
            {
                Logger.getLogger(PaymentForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            doc.open();
            doc.add(new Paragraph(jlblPatientID.getText()));
            doc.add(new Paragraph(jComboBox1.getSelectedItem().toString()));
            doc.add(new Paragraph(jlblPatientName.getText()));
            doc.add(new Paragraph(jComboBox2.getSelectedItem().toString()));
            doc.add(new Paragraph(jlblTreatment.getText()));
            doc.add(new Paragraph(jComboBox3.getSelectedItem().toString()));
            doc.add(new Paragraph(jlblPayment.getText()));
            doc.add(new Paragraph(jtxtPayment.getText()));
            doc.close();

            JOptionPane.showMessageDialog(null, "Your Report Already Save!");
        }catch(DocumentException ex)
        {
            Logger.getLogger(PaymentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbtnPrintActionPerformed

