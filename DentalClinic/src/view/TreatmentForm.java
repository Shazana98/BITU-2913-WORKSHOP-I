    private void jbtnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnInsertActionPerformed
        Connection conn = MySqlDatabase.ConnectDB();
        String query = "INSERT INTO treatment_table(treatmentNum, date, patientID, patientName, treatment, medicines)" + "values(?,?,?,?,?,?)";
        try{
            PreparedStatement ps = conn.prepareStatement(query);
            ps.setString(1, jtxtTreatmentNum.getText());
            ps.setString(2, jtxtDate.getText());
            ps.setString(3, (String) jCmbBoxPatID.getSelectedItem());
            ps.setString(4, (String) jCmbBoxPatName.getSelectedItem());
            ps.setString(5, jtxtTreatment.getText());
            ps.setString(6, jtxtMedicines.getText());
            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Insertion Successful");
            
            TreatmentForm treatmentForm = new TreatmentForm();
            treatmentForm.setVisible(true);
            dispose();
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jbtnInsertActionPerformed

