    private void jbtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnUpdateActionPerformed
    if(jtxtTreatmentNum.getText() != null)
        {
            String UpdateQuery = null;
            PreparedStatement ps = null;
            Connection conn = MySqlDatabase.ConnectDB();
      
            try{
                UpdateQuery = "UPDATE treatment_table SET date = ?, patientID = ?, patientName = ?, treatment = ?, medicines = ? WHERE treatmentNum = ?";
                ps = conn.prepareStatement(UpdateQuery);
                ps.setString(1, jtxtDate.getText());
                ps.setString(2, (String) jCmbBoxPatID.getSelectedItem());
                ps.setString(3, (String) jCmbBoxPatName.getSelectedItem());
                ps.setString(4, jtxtTreatment.getText());
                ps.setString(5, jtxtMedicines.getText());
                
                ps.setString(6, jtxtTreatmentNum.getText());
                ps.executeUpdate();

                JOptionPane.showMessageDialog(null, "Successfully update the data.");
                
                TreatmentForm treatmentForm = new TreatmentForm();
                treatmentForm.setVisible(true);
                dispose();
            }catch(Exception e)
             {
                JOptionPane.showMessageDialog(null, e);
             }
        }
    }//GEN-LAST:event_jbtnUpdateActionPerformed

