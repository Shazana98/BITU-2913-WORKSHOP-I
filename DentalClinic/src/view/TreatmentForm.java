    public ArrayList<Treatment> treatmentList()
    {
        ArrayList<Treatment> treatmentsList = new ArrayList<>();
        Connection conn = MySqlDatabase.ConnectDB();
        String query = "SELECT * FROM treatment_table";
        try {
        PreparedStatement ps = conn.prepareStatement(query);
        ResultSet rs = ps.executeQuery(query);
        
        Treatment treatment;
        while(rs.next())
        {
            treatment = new Treatment(rs.getString("treatmentNum"), rs.getString("date"), rs.getString("patientID"), rs.getString("patientName"), rs.getString("treatment"), rs.getString("medicines"));
            treatmentsList.add(treatment);
        }
        }catch(Exception e)
         {
           JOptionPane.showMessageDialog(null, e);
         }
        return treatmentsList;
    }
    
    public void show_treatment()
    {
        ArrayList<Treatment> list = treatmentList();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Object[] row = new Object[11];
        for(int i=0; i<list.size(); i++)
        {
            row[0] = list.get(i).getTreatmentNum();
            row[1] = list.get(i).getDate();
            row[2] = list.get(i).getPatientID();
            row[3] = list.get(i).getPatientName();
            row[4] = list.get(i).getTreatment();
            row[5] = list.get(i).getMedicines();
            model.addRow(row);   
        }
    }

