    private void jBtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnDeleteActionPerformed
        Connection conn = MySqlDatabase.ConnectDB();
        String query = "DELETE FROM medicine_table WHERE medicineID = ?";
        try{
            PreparedStatement ps = conn.prepareStatement(query);
            ps.setString(1, jtxtMedicineID.getText());
            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data successfully deleted!");

            MedicineForm medicineForm = new MedicineForm();
            medicineForm.setVisible(true);
            dispose();
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jBtnDeleteActionPerformed

    private void jBtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnUpdateActionPerformed
        if(jtxtMedicineID.getText() != null)
        {
            String UpdateQuery = null;
            PreparedStatement ps = null;
            Connection conn = MySqlDatabase.ConnectDB();

            try
            {
                UpdateQuery = "UPDATE medicine_table SET medicineName = ?, expiryDate = ?, quantity = ? WHERE medicineID = ?";
                ps = conn.prepareStatement(UpdateQuery);
                ps.setString(1, jtxtMedicineName.getText());
                ps.setString(2, jTxtDate.getText());
                ps.setString(3, jtxtQuantity.getText());

                ps.setString(4, jtxtMedicineID.getText());
                ps.executeUpdate();

                JOptionPane.showMessageDialog(null, "Data successfully updated!");

                MedicineForm medicineForm = new MedicineForm();
                medicineForm.setVisible(true);
                dispose();
            }catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Medicine ID you have enter does not found!");
        }
    }//GEN-LAST:event_jBtnUpdateActionPerformed

