    public ArrayList<Medicines> medicineList()
    {
        ArrayList<Medicines> medicinesList = new ArrayList<>();
        Connection conn = MySqlDatabase.ConnectDB();
        String query = "SELECT * FROM medicine_table";
        try {
        PreparedStatement ps = conn.prepareStatement(query);
        ResultSet rs = ps.executeQuery(query);
        
        Medicines medicines;
        while(rs.next())
        {
            medicines = new Medicines(rs.getString ("medicineID"), rs.getString ("medicineName"), rs.getString ("expiryDate"), rs.getString ("quantity"));
            medicinesList.add(medicines);
        }
        }catch(Exception e)
         {
           JOptionPane.showMessageDialog(null, e);
         }
        return medicinesList;
    }
    
    public void show_medicine()
    {
        ArrayList<Medicines> list = medicineList();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Object[] row = new Object[11];
        for(int i=0; i<list.size(); i++)
        {
            row[0] = list.get(i).getMedicinesID();
            row[1] = list.get(i).getMedicineName();
            row[2] = list.get(i).getExpiryDate();
            row[3] = list.get(i).getQuantity();
            model.addRow(row);   
        }
    }

